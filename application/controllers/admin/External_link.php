    exit('No direct script access allowed');

class External_link extends CI_Controller
{
    function __construct()
    {
        parent::__construct();
        $this->load->model('External_link_model');
        $this->load->library('form_validation');        
	    $this->load->library('datatables');
    }

    public function index()
    {
        $this->slice->view('admin/external_link/external_link_list');
    } 
    
    public function json() {
        header('Content-Type: application/json');
        echo $this->External_link_model->json();
    }

    public function read($id) 
    {
        $row = $this->External_link_model->get_by_id($id);
        if ($row) {
            $data = array(
    		'id' => $row->id,
    		'title' => $row->title,
    		'url' => $row->url,
	    );
            $this->slice->view('admin/external_link/external_link_read', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('admin/external_link'));
        }
    }

    public function create() 
    {
        $data = array(
            'button' => 'Create',
            'action' => site_url('admin/external_link/create_action'),
	    'id' => set_value('id'),
	    'title' => set_value('title'),
	    'url' => set_value('url'),
	);
        $this->slice->view('admin/external_link/external_link_form', $data);
    }
    
    public function create_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->create();
        } else {
            $data = array(
		'title' => $this->input->post('title',TRUE),
		'url' => $this->input->post('url',TRUE),
	    );

            $this->External_link_model->insert($data);
            $this->session->set_flashdata('message', 'Create Record Success');
            redirect(site_url('admin/external_link'));
        }
    }
    
    public function update($id) 
    {
        $row = $this->External_link_model->get_by_id($id);

        if ($row) {
            $data = array(
                'button' => 'Update',
                'action' => site_url('admin/external_link/update_action'),
        		'id' => set_value('id', $row->id),
        		'title' => set_value('title', $row->title),
        		'url' => set_value('url', $row->url),
	    );
            $this->slice->view('admin/external_link/external_link_form', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('admin/external_link'));
        }
    }
    
    public function update_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->update($this->input->post('id', TRUE));
        } else {
            $data = array(
		'title' => $this->input->post('title',TRUE),
		'url' => $this->input->post('url',TRUE),
	    );

            $this->External_link_model->update($this->input->post('id', TRUE), $data);
            $this->session->set_flashdata('message', 'Update Record Success');
            redirect(site_url('admin/external_link'));
        }
    }
    
    public function delete($id) 
    {
        $row = $this->External_link_model->get_by_id($id);

        if ($row) {
            $this->External_link_model->delete($id);
            $this->session->set_flashdata('message', 'Delete Record Success');
            redirect(site_url('admin/external_link'));
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('admin/external_link'));
        }
    }

    public function _rules() 
    {
	$this->form_validation->set_rules('title', 'title', 'trim|required');
	$this->form_validation->set_rules('url', 'url', 'trim|required');

	$this->form_validation->set_rules('id', 'id', 'trim');
	$this->form_validation->set_error_delimiters('<span class="text-danger">', '</span>');
    }

}

